//! # x86-64 Security Hardening Module
//!
//! This module implements three x86-64-specific security features for the `bcc`
//! C compiler, activated by GCC-compatible command-line flags:
//!
//! ## 1. Retpoline Thunk Generation (`-mretpoline`)
//!
//! Replaces indirect `call *%reg` instructions with calls to speculative-
//! execution-safe "retpoline" thunk sequences, mitigating Spectre v2 / Branch
//! Target Injection (BTI) attacks. Retpoline thunks are generated for registers
//! commonly used in indirect calls: RAX, RCX, RDX, and R11.
//!
//! The canonical retpoline sequence for `%rax`:
//!
//! ```text
//! __x86_retpoline_rax:
//!     call .Lsetup_rax          ; push return address onto stack
//! .Lcapture_spec_rax:
//!     pause                     ; keep speculation loop lightweight
//!     lfence                    ; serialise speculative execution
//!     jmp .Lcapture_spec_rax    ; infinite loop (never actually taken)
//! .Lsetup_rax:
//!     mov %rax, (%rsp)          ; overwrite return address with target
//!     ret                       ; return to actual target, NOT .Lcapture_spec
//! ```
//!
//! ## 2. Intel CET `endbr64` Insertion (`-fcf-protection`)
//!
//! Inserts `endbr64` (End Branch 64-bit) instructions at all function entries
//! and indirect branch targets. This enables Intel's Control-flow Enforcement
//! Technology (CET) Indirect Branch Tracking (IBT), which raises a `#CP`
//! exception if an indirect branch lands on a non-`endbr64` instruction.
//!
//! The `endbr64` instruction encodes as 4 bytes: `F3 0F 1E FA`.
//!
//! ## 3. Automatic Stack Probing (frames > 4096 bytes)
//!
//! Generates page-straddling probe loops for function stack frames that exceed
//! one memory page (4096 bytes). This ensures the OS guard page is hit before
//! any writes to the newly allocated stack space, preventing "stack clash"
//! attacks where an attacker could skip past the guard page with a large
//! allocation.
//!
//! ## Zero External Dependencies
//!
//! This module uses only the Rust standard library (`std`) and internal crate
//! modules. No external crates are imported.

use crate::codegen::{
    CodeGenError, MachineInstr, MachineOperand, Section, SectionFlags, SectionType,
};
use crate::codegen::regalloc::PhysReg;
use crate::codegen::x86_64::abi::{RAX, RCX, RDX, RSP, R11};
use crate::codegen::x86_64::isel::{opcodes, CondCode};

// ============================================================================
// Constants
// ============================================================================

/// Standard page size for stack probing (4096 bytes = one x86 page).
const PAGE_SIZE: u32 = 4096;

/// Maximum number of pages to unroll in stack probing before switching
/// to a loop. Frames up to `PROBE_UNROLL_LIMIT * PAGE_SIZE` bytes generate
/// unrolled probe sequences; larger frames use a counted loop.
const PROBE_UNROLL_LIMIT: u32 = 8;

/// Registers for which retpoline thunks are generated. These are the
/// registers most commonly used for indirect calls/jumps in x86-64 code
/// generated by C compilers.
const RETPOLINE_REGS: [(PhysReg, &str); 4] = [
    (RAX, "rax"),
    (RCX, "rcx"),
    (RDX, "rdx"),
    (R11, "r11"),
];

/// Internal label ID for the stack probe loop back-edge target.
/// Uses a high value unlikely to collide with user-generated labels.
const PROBE_LOOP_LABEL: u32 = 0xFFFF_FF00;

// ============================================================================
// SecurityConfig — configuration for security hardening features
// ============================================================================

/// Configuration controlling which x86-64 security hardening features are
/// enabled for a compilation.
///
/// All fields correspond to GCC-compatible CLI flags. Stack probing is always
/// active with a default threshold of 4096 bytes (one page), while retpoline
/// and CET require explicit opt-in via `-mretpoline` and `-fcf-protection`.
#[derive(Debug, Clone)]
pub struct SecurityConfig {
    /// Enable retpoline thunks for indirect branches (`-mretpoline`).
    ///
    /// When `true`, all indirect `call *%reg` instructions are replaced with
    /// calls to speculative-execution-safe retpoline thunk sequences.
    pub retpoline: bool,

    /// Enable Intel CET `endbr64` insertion (`-fcf-protection`).
    ///
    /// When `true`, `endbr64` instructions are inserted at function entries
    /// and indirect branch targets to support Intel's Indirect Branch Tracking.
    pub cf_protection: bool,

    /// Stack frame size threshold (in bytes) above which stack probing is
    /// emitted. Frames with total size exceeding this value will include
    /// probe sequences that touch every page between the old and new RSP.
    ///
    /// Default: 4096 (one x86 page). This is always active.
    pub stack_probe_threshold: u32,
}

impl Default for SecurityConfig {
    /// Returns a default configuration with all optional features disabled
    /// and the standard stack probe threshold of 4096 bytes.
    fn default() -> Self {
        Self {
            retpoline: false,
            cf_protection: false,
            stack_probe_threshold: PAGE_SIZE,
        }
    }
}

impl SecurityConfig {
    /// Create a `SecurityConfig` from CLI flag values.
    ///
    /// - `retpoline`: corresponds to the `-mretpoline` flag.
    /// - `cf_protection`: corresponds to the `-fcf-protection` flag.
    ///
    /// Stack probing is always enabled with the default threshold (4096 bytes).
    pub fn from_flags(retpoline: bool, cf_protection: bool) -> Self {
        Self {
            retpoline,
            cf_protection,
            stack_probe_threshold: PAGE_SIZE,
        }
    }
}

// ============================================================================
// RetpolineThunk — a self-contained retpoline code sequence
// ============================================================================

/// A retpoline thunk for a specific register.
///
/// Each thunk is a small self-contained function that replaces an indirect
/// `call *%reg` with a speculative-execution-safe sequence. The thunk's
/// symbol name follows the GCC convention `__x86_retpoline_<reg>`.
///
/// The thunk code is 17 bytes:
/// - `call .Lsetup` (5 bytes: E8 + rel32)
/// - `pause` (2 bytes: F3 90)
/// - `lfence` (3 bytes: 0F AE E8)
/// - `jmp .Lcapture_spec` (2 bytes: EB + rel8)
/// - `mov %reg, (%rsp)` (4 bytes: REX.W 89 ModR/M SIB)
/// - `ret` (1 byte: C3)
#[derive(Debug, Clone)]
pub struct RetpolineThunk {
    /// Symbol name, e.g. `"__x86_retpoline_rax"`.
    pub name: String,

    /// The physical register this thunk handles.
    pub register: PhysReg,

    /// Raw machine code bytes of the thunk sequence (17 bytes).
    pub code: Vec<u8>,
}

// ============================================================================
// SecurityHardening — main entry point for security features
// ============================================================================

/// Main entry point for applying x86-64 security hardening features.
///
/// Instantiate with a [`SecurityConfig`] and call methods to:
/// - Generate retpoline thunks and replace indirect calls
/// - Insert `endbr64` instructions at function entries and indirect targets
/// - Generate stack probing sequences for large frames
/// - Apply all features at once via [`apply_security_hardening`]
pub struct SecurityHardening {
    config: SecurityConfig,
}

impl SecurityHardening {
    /// Create a new `SecurityHardening` instance with the given configuration.
    pub fn new(config: SecurityConfig) -> Self {
        Self { config }
    }

    // ========================================================================
    // Retpoline Thunk Generation (-mretpoline)
    // ========================================================================

    /// Generate retpoline thunks for all commonly-used indirect call registers.
    ///
    /// Returns thunks for RAX, RCX, RDX, and R11. Each thunk is a self-
    /// contained code sequence with a known symbol name that indirect calls
    /// are rewritten to target.
    ///
    /// Returns an empty vector if retpoline is not enabled.
    pub fn generate_retpoline_thunks(&self) -> Vec<RetpolineThunk> {
        if !self.config.retpoline {
            return Vec::new();
        }
        RETPOLINE_REGS
            .iter()
            .map(|&(reg, name)| {
                let thunk_name = format!("__x86_retpoline_{}", name);
                encode_retpoline_thunk(reg, &thunk_name)
            })
            .collect()
    }

    /// Returns `true` if the given instruction is an indirect call through a
    /// register that should be replaced with a retpoline call.
    ///
    /// Currently detects instructions with opcode [`opcodes::CALL_R`]
    /// (indirect call via register) whose first operand is a
    /// [`MachineOperand::Register`].
    pub fn needs_retpoline_replacement(&self, instr: &MachineInstr) -> bool {
        if !self.config.retpoline {
            return false;
        }
        // Detect indirect calls via register: opcode is CALL_R and
        // the first operand is a physical register.
        if instr.opcode == opcodes::CALL_R {
            if let Some(MachineOperand::Register(reg)) = instr.operands.first() {
                // Verify the register has a retpoline thunk available.
                // Access the inner u16 field to validate.
                let _encoding = reg.0;
                return retpoline_thunk_name_for_reg(*reg).is_some();
            }
        }
        false
    }

    /// Replace all indirect `call *%reg` instructions in the given vector
    /// with direct calls to the corresponding retpoline thunk symbol.
    ///
    /// For example, `call *%rax` (CALL_R with Register(RAX)) is replaced
    /// with `call __x86_retpoline_rax` (CALL with Symbol).
    ///
    /// Does nothing if retpoline is not enabled.
    pub fn apply_retpoline(&self, instrs: &mut Vec<MachineInstr>) {
        if !self.config.retpoline {
            return;
        }

        for i in 0..instrs.len() {
            if instrs[i].opcode != opcodes::CALL_R {
                continue;
            }

            // Extract the target register from the indirect call.
            let reg = match instrs[i].operands.first() {
                Some(MachineOperand::Register(r)) => *r,
                _ => continue,
            };

            // Look up the retpoline thunk symbol name for this register.
            if let Some(thunk_name) = retpoline_thunk_name_for_reg(reg) {
                // Preserve the source location from the original instruction
                // so DWARF debug info remains accurate.
                let loc = instrs[i].loc.clone();

                // Replace with a direct call to the retpoline thunk.
                let mut replacement = MachineInstr::with_operands(
                    opcodes::CALL,
                    vec![MachineOperand::Symbol(thunk_name)],
                );
                replacement.loc = loc;
                instrs[i] = replacement;
            }
        }
    }

    // ========================================================================
    // Intel CET endbr64 Insertion (-fcf-protection)
    // ========================================================================

    /// Insert an `endbr64` instruction at the beginning of a function's
    /// machine code byte sequence.
    ///
    /// This marks the function entry as a valid indirect branch target for
    /// Intel CET. The 4-byte `endbr64` encoding (`F3 0F 1E FA`) is prepended
    /// at byte offset 0 of the function's code.
    ///
    /// Does nothing if CF protection is not enabled.
    pub fn insert_endbr64_at_function_entry(&self, code: &mut Vec<u8>) {
        if !self.config.cf_protection {
            return;
        }
        let endbr64_bytes = encode_endbr64();
        // Prepend the 4-byte endbr64 at the very start of the function.
        code.splice(0..0, endbr64_bytes.iter().copied());
    }

    /// Insert `endbr64` machine instructions at basic block entries that
    /// are targets of indirect branches.
    ///
    /// The `labels` parameter contains label IDs (basic block identifiers or
    /// instruction indices) where indirect branches may land. An ENDBR64
    /// [`MachineInstr`] is inserted at each corresponding position.
    ///
    /// Labels are interpreted as instruction indices into the `instrs` vector.
    /// Insertions are performed in reverse order to preserve index validity.
    ///
    /// Does nothing if CF protection is not enabled or `labels` is empty.
    pub fn insert_endbr64_at_indirect_targets(
        &self,
        instrs: &mut Vec<MachineInstr>,
        labels: &[u32],
    ) {
        if !self.config.cf_protection || labels.is_empty() {
            return;
        }

        let endbr64_instr = MachineInstr::with_operands(opcodes::ENDBR64, vec![]);

        // Deduplicate and sort label positions.
        let mut positions: Vec<usize> = labels
            .iter()
            .map(|&l| l as usize)
            .filter(|&idx| idx <= instrs.len())
            .collect();
        positions.sort_unstable();
        positions.dedup();

        // Insert in reverse order so earlier indices remain valid.
        for &pos in positions.iter().rev() {
            instrs.insert(pos, endbr64_instr.clone());
        }
    }

    // ========================================================================
    // Stack Probing for Large Frames
    // ========================================================================

    /// Returns `true` if a function with the given `frame_size` (in bytes)
    /// requires stack probing.
    ///
    /// Stack probing is needed when the frame size exceeds the configured
    /// threshold (default: 4096 bytes = one page).
    pub fn needs_stack_probe(&self, frame_size: u32) -> bool {
        frame_size > self.config.stack_probe_threshold
    }

    /// Generate a stack probe sequence as raw machine code bytes.
    ///
    /// The probe touches every page between the current RSP and the desired
    /// allocation point (`RSP - frame_size`), ensuring the OS guard page is
    /// triggered before any writes to the new stack region.
    ///
    /// For frames up to `PROBE_UNROLL_LIMIT` pages the probe is unrolled.
    /// For larger frames a counted loop using RAX is emitted.
    ///
    /// Returns an empty vector if the frame does not require probing.
    pub fn generate_stack_probe(&self, frame_size: u32) -> Vec<u8> {
        if !self.needs_stack_probe(frame_size) {
            return Vec::new();
        }

        let pages = frame_size / PAGE_SIZE;
        let remainder = frame_size % PAGE_SIZE;

        if pages <= PROBE_UNROLL_LIMIT {
            self.encode_unrolled_probe(pages, remainder)
        } else {
            self.encode_loop_probe(frame_size)
        }
    }

    /// Generate a stack probe as a sequence of [`MachineInstr`] values.
    ///
    /// This is the instruction-level equivalent of [`generate_stack_probe`],
    /// producing abstract machine instructions that integrate with the
    /// instruction selection pipeline rather than raw bytes.
    ///
    /// For frames up to `PROBE_UNROLL_LIMIT` pages the probe is unrolled.
    /// For larger frames a counted loop using RAX as a page counter is emitted.
    ///
    /// Returns an empty vector if the frame does not require probing.
    pub fn generate_stack_probe_instrs(&self, frame_size: u32) -> Vec<MachineInstr> {
        if !self.needs_stack_probe(frame_size) {
            return Vec::new();
        }

        let pages = frame_size / PAGE_SIZE;
        let remainder = frame_size % PAGE_SIZE;

        if pages <= PROBE_UNROLL_LIMIT {
            self.generate_unrolled_probe_instrs(pages, remainder)
        } else {
            self.generate_loop_probe_instrs(pages, remainder)
        }
    }

    // ========================================================================
    // Integration API
    // ========================================================================

    /// Apply all enabled security hardening features to a function's machine
    /// code byte stream.
    ///
    /// This is the high-level entry point called by the x86-64 backend after
    /// instruction selection and encoding. It applies, in order:
    ///
    /// 1. **CET `endbr64`** at function entry (if `cf_protection` is enabled
    ///    and `is_function_entry` is `true`).
    /// 2. **Stack probing** for large frames (if `frame_size` exceeds the
    ///    configured threshold).
    /// 3. **Retpoline** note: retpoline replacement operates at the
    ///    [`MachineInstr`] level via [`apply_retpoline`] before encoding.
    ///    The `has_indirect_branches` flag is informational here.
    ///
    /// # Parameters
    ///
    /// - `code`: The function's raw machine code bytes (modified in place).
    /// - `frame_size`: Total stack frame size in bytes.
    /// - `is_function_entry`: Whether this code represents a function entry.
    /// - `has_indirect_branches`: Whether the function contains indirect
    ///   call/jump instructions (informational; retpoline is applied at the
    ///   MachineInstr level).
    pub fn apply_security_hardening(
        &self,
        code: &mut Vec<u8>,
        frame_size: u32,
        is_function_entry: bool,
        _has_indirect_branches: bool,
    ) {
        // 1. Insert endbr64 at function entry if CET is enabled.
        if self.config.cf_protection && is_function_entry {
            self.insert_endbr64_at_function_entry(code);
        }

        // 2. Insert stack probe if frame exceeds threshold.
        if self.needs_stack_probe(frame_size) {
            let probe_bytes = self.generate_stack_probe(frame_size);
            // Insert probe after any endbr64 that was just prepended.
            let insert_pos = if self.config.cf_protection && is_function_entry {
                4 // After the 4-byte endbr64
            } else {
                0
            };
            code.splice(insert_pos..insert_pos, probe_bytes.iter().copied());
        }

        // 3. Retpoline replacement is handled at the MachineInstr level
        //    (apply_retpoline) before this function is called. The raw byte
        //    stream already contains direct calls to retpoline thunk symbols.
    }

    /// Returns additional `.text` sections containing retpoline thunk code.
    ///
    /// These sections should be appended to the object code by the backend's
    /// top-level code generation module. Each retpoline thunk gets its own
    /// section so the linker can deduplicate identical thunks across
    /// compilation units if desired.
    ///
    /// Returns an empty vector if retpoline is not enabled.
    pub fn get_retpoline_sections(&self) -> Vec<Section> {
        if !self.config.retpoline {
            return Vec::new();
        }

        let thunks = self.generate_retpoline_thunks();
        thunks
            .into_iter()
            .map(|thunk| Section {
                name: format!(".text.{}", thunk.name),
                data: thunk.code,
                section_type: SectionType::Text,
                alignment: 16,
                flags: SectionFlags {
                    writable: false,
                    executable: true,
                    allocatable: true,
                },
            })
            .collect()
    }

    // ========================================================================
    // Private helpers — unrolled stack probe (raw bytes)
    // ========================================================================

    /// Encode an unrolled stack probe sequence as raw machine code bytes.
    ///
    /// Emits one `sub rsp, 4096` + `test [rsp], rsp` pair per page,
    /// followed by a final `sub rsp, remainder` if non-zero.
    fn encode_unrolled_probe(&self, pages: u32, remainder: u32) -> Vec<u8> {
        let mut bytes = Vec::with_capacity((pages as usize) * 11 + 7);

        for _ in 0..pages {
            // sub rsp, 4096
            // REX.W(48) 81 EC imm32(00 10 00 00)
            bytes.extend_from_slice(&[0x48, 0x81, 0xEC]);
            bytes.extend_from_slice(&PAGE_SIZE.to_le_bytes());

            // test [rsp], rsp — touch the page (read probe)
            // REX.W(48) 85(TEST r/m64,r64) 24(ModR/M) 24(SIB)
            bytes.extend_from_slice(&[0x48, 0x85, 0x24, 0x24]);
        }

        // Final remainder adjustment (if non-zero).
        if remainder > 0 {
            // sub rsp, remainder
            bytes.extend_from_slice(&[0x48, 0x81, 0xEC]);
            bytes.extend_from_slice(&remainder.to_le_bytes());
        }

        bytes
    }

    /// Encode a loop-based stack probe as raw machine code bytes.
    ///
    /// Uses RAX as a temporary to hold the target RSP value. The loop
    /// decrements RSP by one page per iteration and probes each page
    /// until RSP reaches the target.
    ///
    /// ```text
    /// mov rax, rsp              ; save current RSP
    /// sub rax, <frame_size>     ; compute target = rsp - N
    /// .Lprobe_loop:
    ///     sub rsp, 4096         ; allocate one page
    ///     test [rsp], rsp       ; touch (probe) the new page
    ///     cmp rsp, rax          ; compare against target
    ///     ja .Lprobe_loop       ; loop while rsp > target
    /// mov rsp, rax              ; set final rsp to exact target
    /// ```
    fn encode_loop_probe(&self, frame_size: u32) -> Vec<u8> {
        let mut bytes = Vec::with_capacity(32);

        // mov rax, rsp — save current RSP to RAX.
        // REX.W(48) 89(MOV r/m64,r64) E0(mod=11 reg=RSP(4) r/m=RAX(0))
        bytes.extend_from_slice(&[0x48, 0x89, 0xE0]);

        // sub rax, frame_size — compute target RSP value.
        // REX.W(48) 2D(SUB RAX,imm32) imm32
        bytes.extend_from_slice(&[0x48, 0x2D]);
        bytes.extend_from_slice(&frame_size.to_le_bytes());

        // .Lprobe_loop: (byte offset of loop body start)
        let loop_start = bytes.len();

        // sub rsp, 4096 — allocate one page.
        bytes.extend_from_slice(&[0x48, 0x81, 0xEC]);
        bytes.extend_from_slice(&PAGE_SIZE.to_le_bytes());

        // test [rsp], rsp — probe the page.
        bytes.extend_from_slice(&[0x48, 0x85, 0x24, 0x24]);

        // cmp rsp, rax — compare current RSP against target.
        // REX.W(48) 39(CMP r/m64,r64) C4(mod=11 reg=RAX(0) r/m=RSP(4))
        bytes.extend_from_slice(&[0x48, 0x39, 0xC4]);

        // ja .Lprobe_loop — loop if RSP > target (unsigned above).
        // 77(JA rel8)  rel8 = loop_start - (current + 2)
        let ja_pos = bytes.len();
        let rel8 = (loop_start as i32) - (ja_pos as i32 + 2);
        bytes.push(0x77);
        bytes.push(rel8 as u8);

        // mov rsp, rax — set final RSP to exact target.
        // REX.W(48) 89(MOV r/m64,r64) C4(mod=11 reg=RAX(0) r/m=RSP(4))
        bytes.extend_from_slice(&[0x48, 0x89, 0xC4]);

        bytes
    }

    // ========================================================================
    // Private helpers — unrolled stack probe (MachineInstr)
    // ========================================================================

    /// Generate an unrolled stack probe as [`MachineInstr`] sequences.
    ///
    /// One SUB+TEST pair per page, with a final SUB for any remainder.
    fn generate_unrolled_probe_instrs(
        &self,
        pages: u32,
        remainder: u32,
    ) -> Vec<MachineInstr> {
        let mut instrs = Vec::with_capacity((pages as usize) * 2 + 1);

        for _ in 0..pages {
            // sub rsp, 4096
            instrs.push(MachineInstr::with_operands(
                opcodes::SUB_RI,
                vec![
                    MachineOperand::Register(RSP),
                    MachineOperand::Immediate(PAGE_SIZE as i64),
                ],
            ));

            // test [rsp], rsp — probe the page
            instrs.push(MachineInstr::with_operands(
                opcodes::TEST_RR,
                vec![
                    MachineOperand::Memory {
                        base: RSP,
                        offset: 0,
                    },
                    MachineOperand::Register(RSP),
                ],
            ));
        }

        // Final remainder adjustment.
        if remainder > 0 {
            instrs.push(MachineInstr::with_operands(
                opcodes::SUB_RI,
                vec![
                    MachineOperand::Register(RSP),
                    MachineOperand::Immediate(remainder as i64),
                ],
            ));
        }

        instrs
    }

    /// Generate a loop-based stack probe as [`MachineInstr`] sequences.
    ///
    /// Uses RAX as a temporary to hold the target RSP and a counted loop
    /// approach for frames exceeding the unroll threshold.
    ///
    /// Instruction sequence:
    /// ```text
    /// mov rax, rsp              ; save current rsp
    /// sub rax, total            ; compute target rsp
    /// loop:
    ///   sub rsp, 4096           ; allocate one page
    ///   test [rsp], rsp         ; probe
    ///   cmp rsp, rax            ; compare to target
    ///   ja loop                 ; loop while rsp > target
    /// mov rsp, rax              ; set final rsp
    /// ```
    fn generate_loop_probe_instrs(
        &self,
        pages: u32,
        remainder: u32,
    ) -> Vec<MachineInstr> {
        let mut instrs = Vec::with_capacity(8);
        let total = (pages as i64) * (PAGE_SIZE as i64) + (remainder as i64);

        // mov rax, rsp — save current RSP to compute target.
        instrs.push(MachineInstr::with_operands(
            opcodes::MOV_RR,
            vec![
                MachineOperand::Register(RAX),
                MachineOperand::Register(RSP),
            ],
        ));

        // sub rax, total — compute target RSP value.
        instrs.push(MachineInstr::with_operands(
            opcodes::SUB_RI,
            vec![
                MachineOperand::Register(RAX),
                MachineOperand::Immediate(total),
            ],
        ));

        // Loop body start — the JCC below references this label.

        // sub rsp, 4096 — allocate one page.
        instrs.push(MachineInstr::with_operands(
            opcodes::SUB_RI,
            vec![
                MachineOperand::Register(RSP),
                MachineOperand::Immediate(PAGE_SIZE as i64),
            ],
        ));

        // test [rsp], rsp — probe the page.
        instrs.push(MachineInstr::with_operands(
            opcodes::TEST_RR,
            vec![
                MachineOperand::Memory {
                    base: RSP,
                    offset: 0,
                },
                MachineOperand::Register(RSP),
            ],
        ));

        // cmp rsp, <target> — compare current RSP against target in RAX.
        // Uses CMP_RI opcode with register operands; the encoder resolves
        // the correct encoding (CMP r/m64, r64) based on operand types.
        instrs.push(MachineInstr::with_operands(
            opcodes::CMP_RI,
            vec![
                MachineOperand::Register(RSP),
                MachineOperand::Register(RAX),
            ],
        ));

        // ja .Lprobe_loop — loop while RSP > target (CondCode::A).
        instrs.push(MachineInstr::with_operands(
            opcodes::JCC,
            vec![
                MachineOperand::Immediate(CondCode::A as i64),
                MachineOperand::Label(PROBE_LOOP_LABEL),
            ],
        ));

        // mov rsp, rax — set final RSP to the exact target value.
        instrs.push(MachineInstr::with_operands(
            opcodes::MOV_RR,
            vec![
                MachineOperand::Register(RSP),
                MachineOperand::Register(RAX),
            ],
        ));

        instrs
    }
}

// ============================================================================
// Standalone public function — encode_endbr64
// ============================================================================

/// Encode the `endbr64` instruction as raw bytes.
///
/// Returns the 4-byte encoding `[0xF3, 0x0F, 0x1E, 0xFA]` which marks a
/// valid indirect branch target for Intel CET (Control-flow Enforcement
/// Technology).
///
/// This is a standalone function so it can be used without instantiating
/// [`SecurityHardening`], e.g. during direct machine code emission.
pub fn encode_endbr64() -> [u8; 4] {
    [0xF3, 0x0F, 0x1E, 0xFA]
}

