    #[test]
    fn test_retpoline_thunk_rcx_encoding() {
        let config = SecurityConfig::from_flags(true, false);
        let hardening = SecurityHardening::new(config);
        let thunks = hardening.generate_retpoline_thunks();

        let rcx_thunk = &thunks[1];
        assert_eq!(rcx_thunk.register, RCX);

        // mov %rcx, (%rsp): 48 89 0C 24
        // RCX encoding = 1, ModR/M = (1 << 3) | 4 = 0x0C
        assert_eq!(rcx_thunk.code[12], 0x48);
        assert_eq!(rcx_thunk.code[13], 0x89);
        assert_eq!(rcx_thunk.code[14], 0x0C);
        assert_eq!(rcx_thunk.code[15], 0x24);
    }

