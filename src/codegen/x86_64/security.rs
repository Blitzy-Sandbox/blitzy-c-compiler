/// Encode a complete retpoline thunk for the given register.
///
/// The thunk sequence (17 bytes) is:
///
/// ```text
/// offset 0:  E8 07 00 00 00       call .Lsetup (rel32 = +7)
/// offset 5:  F3 90                pause
/// offset 7:  0F AE E8             lfence
/// offset 10: EB F9                jmp .Lcapture_spec (rel8 = -7)
/// offset 12: <mov %reg, (%rsp)>   4 bytes (REX.W + 89 + ModR/M + SIB)
/// offset 16: C3                   ret
/// ```
fn encode_retpoline_thunk(reg: PhysReg, name: &str) -> RetpolineThunk {
    let mut code = Vec::with_capacity(17);

    // call .Lsetup — E8 rel32
    // .Lsetup is at offset 12, call ends at offset 5, so rel32 = 12 - 5 = 7
    code.push(0xE8);
    code.extend_from_slice(&7_i32.to_le_bytes());

    // .Lcapture_spec: (offset 5)
    // pause — F3 90
    code.push(0xF3);
    code.push(0x90);

    // lfence — 0F AE E8
    code.push(0x0F);
    code.push(0xAE);
    code.push(0xE8);

    // jmp .Lcapture_spec — EB rel8
    // .Lcapture_spec is at offset 5, jmp ends at offset 12, so rel8 = 5 - 12 = -7
    code.push(0xEB);
    code.push((-7_i8) as u8);

    // .Lsetup: (offset 12)
    // mov %reg, (%rsp) — encode with REX.W prefix and correct register field
    let reg_idx = reg.0 as u8;
    encode_mov_reg_to_rsp_indirect(&mut code, reg_idx);

    // ret — C3
    code.push(0xC3);

    debug_assert_eq!(code.len(), 17, "retpoline thunk must be exactly 17 bytes");

    RetpolineThunk {
        name: name.to_string(),
        register: reg,
        code,
    }
}

/// Encode `mov %reg, (%rsp)` — stores the register value at the top of
/// the stack (the location where the `call` instruction pushed the return
/// address). This overwrites the return address so that `ret` jumps to
/// the actual indirect target register value.
///
/// # Encoding
///
/// For registers 0–7 (rax, rcx, rdx, rbx, rsp, rbp, rsi, rdi):
///   `REX.W(48)  89  ModR/M  SIB(24)`
///
/// For registers 8–15 (r8–r15):
///   `REX.W+REX.R(4C)  89  ModR/M  SIB(24)`
///
/// ModR/M = `(0b00 << 6) | ((reg & 0x07) << 3) | 0x04`
/// SIB    = `0x24` (base=RSP, no index)
fn encode_mov_reg_to_rsp_indirect(code: &mut Vec<u8>, reg_idx: u8) {
    // REX prefix: W=1 (64-bit operand), R=1 if reg >= 8 (extended register)
    let rex = if reg_idx >= 8 { 0x4C } else { 0x48 };
    code.push(rex);

    // MOV r/m64, r64 opcode
    code.push(0x89);

    // ModR/M: mod=00 (indirect, no displacement), reg=lower 3 bits of
    // register index, r/m=100 (SIB byte follows for RSP addressing)
    let modrm = ((reg_idx & 0x07) << 3) | 0x04;
    code.push(modrm);

    // SIB: scale=00, index=100 (no index when index field = RSP),
    // base=100 (RSP)
    code.push(0x24);
}

/// Look up the retpoline thunk symbol name for a given physical register.
///
/// Returns `Some("__x86_retpoline_<name>")` if the register has a
/// corresponding thunk, or `None` if no thunk is available for that register.
fn retpoline_thunk_name_for_reg(reg: PhysReg) -> Option<String> {
    for &(thunk_reg, name) in &RETPOLINE_REGS {
        if reg == thunk_reg {
            return Some(format!("__x86_retpoline_{}", name));
        }
    }
    None
}

/// Map a condition code enum variant to a descriptive string (for
