    #[test]
    fn test_loop_probe_bytes_ja_target() {
        let config = SecurityConfig::default();
        let hardening = SecurityHardening::new(config);

        // 40960 = 10 pages → loop probe (exceeds unroll limit of 8).
        let bytes = hardening.generate_stack_probe(40960);

        // Verify the JA instruction's relative offset targets the loop start.
        // Structure: mov(3) + sub(6) + [loop: sub(7) + test(4) + cmp(3) + ja(2)]
        //           + mov(3) = 28 bytes total.
        // Loop start offset = 9.
        // JA is at offset 23 (9 + 7 + 4 + 3 = 23).
        // JA occupies 2 bytes (77 rel8), ends at offset 25.
        // rel8 = 9 - 25 = -16 = 0xF0.
        assert_eq!(bytes[23], 0x77, "JA opcode");
        assert_eq!(bytes[24], 0xF0, "JA rel8 = -16 (back to loop start)");
    }
